#!/bin/sh
echo "*********************************************************************************************************"
echo "CLEAN AND RUN MONGODB"
echo "*********************************************************************************************************"
rm -rf /data/db/*
mongod -f /etc/mongod.conf
sleep 10

echo "*********************************************************************************************************"
echo "CREATE LOG FOLDER"  
echo "*********************************************************************************************************"
if [ -d ../../../../../logs/];
   echo "logs directory exists"
else
	mkdir ../../../../../logs
fi

